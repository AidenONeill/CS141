package Lab7;

// use a for loop to print a row of stars to the screen
// each following line will have one less star
//


public class LoopsStar
{
	public static void main (String args[])
	{
		for(int x=10;x>0;x--)
		{

		for(int y=x;y>0;y--)
		{
			System.out.print("*");
		}
		System.out.println();

		}

	}
}

